Map::Map() {
	dirt = TextureManager::LoadTexture("assets/Dirt.png");
	grass = TextureManager::LoadTexture("assets/Grass.png");
	water = TextureManager::LoadTexture("assets/Water.png");

	LoadMap(lvl1);

	source.x = source.y = 0;
	destination.x = destination.y = 0;

	source.w = destination.w = 32;
	source.h = destination.h = 32;
}

Map::~Map() {
}

void Map::LoadMap(BITMAP newMap) {
	bitMap = newMap;
}

void Map::DrawMap() {
	int type = 0;

	for (int row = 0; row < bitMap.size(); row++)
		for (int col = 0; col < bitMap[0].size(); col++) {
			type = bitMap[row][col];

			destination.x = col * 32;
			destination.y = row * 32;

			switch (type) {
				case 0:
					TextureManager::Draw(dirt, source, destination);
					break;
				case 1:
					TextureManager::Draw(grass, source, destination);
					break;
				case 2:
					TextureManager::Draw(water, source, destination);
					break;
				default:
					break;
			}
		}
}

